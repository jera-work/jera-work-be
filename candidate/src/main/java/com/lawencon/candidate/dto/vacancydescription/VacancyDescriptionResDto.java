package com.lawencon.candidate.dto.vacancydescription;

public class VacancyDescriptionResDto {
	private String degreeName;
	private String genderName;
	private String ageVacancyName;
	private String jobTypeName;
	private String salary;
	private String cityName;
	private String address;
	private String description;
	public String getDegreeName() {
		return degreeName;
	}
	public void setDegreeName(String degreeName) {
		this.degreeName = degreeName;
	}
	public String getGenderName() {
		return genderName;
	}
	public void setGenderName(String genderName) {
		this.genderName = genderName;
	}
	public String getAgeVacancyName() {
		return ageVacancyName;
	}
	public void setAgeVacancyName(String ageVacancyName) {
		this.ageVacancyName = ageVacancyName;
	}
	public String getJobTypeName() {
		return jobTypeName;
	}
	public void setJobTypeName(String jobTypeName) {
		this.jobTypeName = jobTypeName;
	}
	public String getSalary() {
		return salary;
	}
	public void setSalary(String salary) {
		this.salary = salary;
	}
	public String getCityName() {
		return cityName;
	}
	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	
}
